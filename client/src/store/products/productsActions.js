import axios from "axios";
import { PRODUCTS_ACTION_TYPES } from "./productsTypes";
import { PRODUCT_ACTION_TYPES } from "../product/productTypes";
import { logout } from "../users/usersActions";

export const fetchProducts =
  (keyword = "", pageNumber = "") =>
  async (dispatch) => {
    try {
      dispatch({ type: PRODUCTS_ACTION_TYPES.PRODUCTS_REQUEST });

      const { data } = await axios.get(
        `/api/products?keyword=${keyword}&pageNumber=${pageNumber}`
      );

      dispatch({ type: PRODUCTS_ACTION_TYPES.PRODUCTS_SUCCESS, payload: data });
    } catch (error) {
      dispatch({
        type: PRODUCTS_ACTION_TYPES.PRODUCTS_FAIL,
        payload:
          error.response && error.response.data.message
            ? error.response.data.message
            : error.message,
      });
    }
  };

export const deleteProduct = (id) => async (dispatch, getState) => {
  try {
    dispatch({
      type: PRODUCTS_ACTION_TYPES.PRODUCT_DELETE_REQUEST,
    });

    const {
      userLogin: { userInfo },
    } = getState();

    const config = {
      headers: {
        Authorization: `Bearer ${userInfo.token}`,
      },
    };

    await axios.delete(`/api/products/${id}`, config);

    dispatch({
      type: PRODUCTS_ACTION_TYPES.PRODUCT_DELETE_SUCCESS,
    });
  } catch (error) {
    const message =
      error.response && error.response.data.message
        ? error.response.data.message
        : error.message;
    if (message === "Not authorized, token failed") {
      dispatch(logout());
    }
    dispatch({
      type: PRODUCTS_ACTION_TYPES.PRODUCT_DELETE_FAIL,
      payload: message,
    });
  }
};

